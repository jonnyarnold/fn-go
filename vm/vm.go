package vm

func RunBytecode() string {
	bytecode := []byte{
		DECLARE_CONSTANT, TYPE_BOOL, 0x00, 0x00,
		DECLARE_CONSTANT, TYPE_BOOL, 0x01, 0x00,
		SET_REGISTER_WITH_CONSTANT, 0x00, 0x00, 0x00,
		SET_REGISTER_WITH_CONSTANT, 0x01, 0x01, 0x00,
		AND, 0x02, 0x01, 0x00,
		DUMP, 0x00, 0x00, 0x00,
		OR, 0x02, 0x01, 0x00,
		TERMINATE, 0x00, 0x00, 0x00,
	}

	machine := NewMachine(bytecode)
	machine.ProcessAll()
	return machine.Result()
}
